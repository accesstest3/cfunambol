package com.funambol.framework.server.inventory;

import com.funambol.framework.core.Sync4jException;

/**
 * Exception class thrown in DeviceInventory interface
 * @see DeviceInventory
 *
 * @version $Id: DeviceInventoryException.java,v 1.2 2007-11-28 11:15:59 nichele Exp $
 */
public class DeviceInventoryException
        extends Sync4jException {

    /**
     * Constructs a new exception with the specified detail message
     * @param s human readable String that describes the cause of the exception
     *
     */
    public DeviceInventoryException(final String s)
    {
        super(s);
    }

    /**
     * Constructs a new exception with the specified detail message and cause
     * @param s the detail message
     * @param throwable the cause
     */
    public DeviceInventoryException(final String s,final Throwable throwable)
    {
        super(s, throwable);
    }

    /**
     * Constructs a new exception with the specified cause
     * @param throwable  the cause
     */
    public DeviceInventoryException(final Throwable throwable)
    {
        super(throwable);
    }
}
