class WMLInitialiser extends WbxmlInitialiser {

    public WMLInitialiser() {
    }
    public void initialise(WbxmlParser aParser) {
        aParser.setTagTable (0, Wml.tagTable);
        aParser.setAttrStartTable (0, Wml.attrStartTable);
        aParser.setAttrValueTable (0, Wml.attrValueTable);
    }
}